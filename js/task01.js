function ageChecker() {
    age = prompt("Whats your age? ");

    if (age <= 17) {
        alert("Sorry you are not allowed to drive yet!");

    } else if (age >= 13) {
        alert("Congrats! You are allowed to drive!");

    } else {
        alert("Enter a Number to verify your Age Please!")
    }
};
